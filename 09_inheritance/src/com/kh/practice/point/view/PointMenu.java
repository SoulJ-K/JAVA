package com.kh.practice.point.view;

import java.util.Scanner;

import com.kh.practice.point.controller.CircleController;
import com.kh.practice.point.controller.RectangleController;

public class PointMenu {
	
	private Scanner sc = new Scanner(System.in);
	private CircleController cc = new CircleController();
	private RectangleController rc = new RectangleController();
	
	public void mainMenu() {
		while(true){
		System.out.println("===== 메뉴 =====");
		System.out.println("1. 원 ");
		System.out.println("2. 사각형");
		System.out.println("9. 끝내기");
		System.out.println("메뉴번호 : ");
		
		int menunum = sc.nextInt();
		sc.nextLine();
		
		switch(menunum) {
		case 1 : circleMenu(); break;
		case 2 : rectangleMenu(); break;
		case 9 : System.out.println("프로그램을 종료합니다."); break;
		default : System.out.println("잘못된 번호입니다. 다시 입력해주세요."); break;
		}
		if(menunum == 9) {
			break;
		}
		
		}
	}
	
	public void circleMenu() {
		while(true){
			System.out.println("===== 원 메뉴 =====");
			System.out.println("1. 원 둘레");
			System.out.println("2. 원 넓이");
			System.out.println("9. 메인으로");
			System.out.println("메뉴번호 : ");
			
			int menunum = sc.nextInt();
			
			switch(menunum) {
			case 1 : calcCircum(); break;
			case 2 : calcCircleArea(); break;
			case 9 : System.out.println("메인으로 돌아갑니다."); break;
			default : System.out.println("잘못된 번호입니다. 다시 입력해주세요."); break;
			}
			if(menunum == 9) {
				break;
			}
			sc.nextLine();
			}
		
	}
	
	public void rectangleMenu() {
		while(true){
			System.out.println("===== 메뉴 =====");
			System.out.println("1. 사각형둘레");
			System.out.println("2. 사각형 넓이");
			System.out.println("9. 메인으로");
			System.out.println("메뉴번호 : ");
			
			int menunum = sc.nextInt();
			sc.nextLine();
			
			switch(menunum) {
			case 1 : calcPerimeter(); break;
			case 2 : calcRectArea(); break;
			case 9 : System.out.println("메인으로 돌아갑니다."); break;
			default : System.out.println("잘못된 번호입니다. 다시 입력해주세요."); break;
			}
			if(menunum == 9) {
				break;			
			}
		}
		
	}
	
	public void calcCircum() {
		System.out.println("x 좌표 : ");
		int x = sc.nextInt();
		System.out.println("y 좌표 : ");
		int y = sc.nextInt();
		System.out.println("반지름 : ");
		int xy = sc.nextInt();
		sc.nextLine();
		
		System.out.println(cc.calcCircum(x, y, xy));
		
	}
	
	public void calcCircleArea() {
		System.out.println("x 좌표 : ");
		int x = sc.nextInt();
		System.out.println("y 좌표 : ");
		int y = sc.nextInt();
		System.out.println("반지름 : ");
		int xy = sc.nextInt();
		sc.nextLine();
		
		System.out.println(cc.calcArea(x, y, xy));
		
	}
	
	public void calcPerimeter() {
		System.out.println("x 좌표 : ");
		int x = sc.nextInt();
		System.out.println("y 좌표 : ");
		int y = sc.nextInt();
		System.out.println("높이 : ");
		int h = sc.nextInt();
		System.out.println("너비 : ");
		int w = sc.nextInt();
		sc.nextLine();
		
		System.out.println(rc.calcPerimeter(x, y, h, w));
		
	}
	
	public void calcRectArea() {
		System.out.println("x 좌표 : ");
		int x = sc.nextInt();
		System.out.println("y 좌표 : ");
		int y = sc.nextInt();
		System.out.println("높이 : ");
		int h = sc.nextInt();
		System.out.println("너비 : ");
		int w = sc.nextInt();
		sc.nextLine();
		
		System.out.println(rc.calcArea(x, y, h, w));
		
	}

}
